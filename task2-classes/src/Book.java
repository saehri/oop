public class Book {
  public int bookId;
  public String title;
  public String authorName;
  public int price;
  public int publisherNumber;

  public Book(int bookId, String title, String authorName, int price, int publisherNumber) {
    this.bookId = bookId;
    this.title = title;
    this.authorName = authorName;
    this.price = price;
    this.publisherNumber = publisherNumber;
  }
}
